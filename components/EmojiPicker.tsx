// Recently used emojis (stored in localStorage)
const getRecentEmojis = (): string[] => {
  if (typeof window === 'undefined') return [];
  try {
    return JSON.parse(localStorage.getItem('recent-emojis') || '[]');
  } catch {
    return [];
  }
};

const saveRecentEmoji = (emoji: string) => {
  if (typeof window === 'undefined') return;
  const recent = getRecentEmojis();
  const updated = [emoji, ...recent.filter(e => e !== emoji)].slice(0, 12);
  localStorage.setItem('recent-emojis', JSON.stringify(updated));
};

export function EmojiPicker({ open, onOpenChange, onEmojiSelect }: EmojiPickerProps) {
  const [searchTerm, setSearchTerm] = useState('');
  const [recentEmojis, setRecentEmojis] = useState<string[]>(getRecentEmojis());

  const filteredEmojis = useMemo(() => {
    if (!searchTerm) return emojiCategories;
    
    const searchLower = searchTerm.toLowerCase();
    return emojiCategories.map(category => ({
      ...category,
      emojis: category.emojis.filter(emoji => 
        category.name.toLowerCase().includes(searchLower)
      )
    })).filter(category => category.emojis.length > 0);
  }, [searchTerm]);

  const handleEmojiSelect = (emoji: string) => {
    saveRecentEmoji(emoji);
    setRecentEmojis(getRecentEmojis());
    onEmojiSelect(emoji);
    onOpenChange(false);
  };

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="max-w-md max-h-[80vh] overflow-hidden">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Smile className="h-5 w-5" />
            Choose an Emoji
          </DialogTitle>
        </DialogHeader>

        <div className="space-y-4">
          {/* Search */}
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              placeholder="Search emojis..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="pl-10"
            />
          </div>

          <ScrollArea className="h-96">
            <div className="space-y-4 pr-4">
              {/* Recent Emojis */}
              {recentEmojis.length > 0 && !searchTerm && (
                <div>
                  <h4 className="text-sm font-medium mb-2 flex items-center gap-2">
                    <Clock className="h-4 w-4" />
                    Recently Used
                  </h4>
                  <div className="grid grid-cols-8 gap-1">
                    {recentEmojis.map((emoji, index) => (
                      <Button
                        key={index}
                        variant="ghost"
                        size="sm"
                        className="h-12 w-12 text-2xl hover:scale-110 transition-transform"
                        onClick={() => handleEmojiSelect(emoji)}
                      >
                        {emoji}
                      </Button>
                    ))}
                  </div>
                </div>
              )}

              {/* Emoji Categories */}
              {filteredEmojis.map((category) => (
                <div key={category.name}>
                  <h4 className="text-sm font-medium mb-2 sticky top-0 bg-background py-1">
                    {category.name}
                  </h4>
                  <div className="grid grid-cols-8 gap-1">
                    {category.emojis.map((emoji, index) => (
                      <Button
                        key={index}
                        variant="ghost"
                        size="sm"
                        className="h-12 w-12 text-2xl hover:scale-110 transition-transform"
                        onClick={() => handleEmojiSelect(emoji)}
                      >
                        {emoji}
                      </Button>
                    ))}
                  </div>
                </div>
              ))}

              {filteredEmojis.length === 0 && (
                <div className="text-center py-8 text-muted-foreground">
                  <Smile className="h-12 w-12 mx-auto mb-2 opacity-50" />
                  <p>No emojis found</p>
                  <p className="text-sm">Try a different search term</p>
                </div>
              )}
            </div>
          </ScrollArea>
        </div>
      </DialogContent>
    </Dialog>
  );
}